function get_bofa_kill_effect($class_id, $path_id, $monster) {
	global $phylum_effects, $regular_effects, $refuse_items;
	$seed = 421 * $class_id + 11 * $path_id + (int)$monster[0];
	mt_srand($seed, MT_RAND_PHP);
	$effect = null;
	if ($seed % 3 == 1) {
		$effect = $phylum_effects[$monster[2]][mt_rand(0, count($phylum_effects[$monster[2]]) - 1)];
	}
	else {
		$effect = $regular_effects[mt_rand(0, count($regular_effects) - 1)];
	}
	if ($effect == "I Refuse!") {
		mt_srand($seed + 11, MT_RAND_PHP);
		$effect = $refuse_items[mt_rand(0, count($refuse_items) - 1)];
	}
	if (is_array($effect)) {
		mt_srand($seed + 13, MT_RAND_PHP);
		$effect = $effect[mt_rand(0, count($effect) - 1)];
	}
	return $effect;
}

function get_monster_zone_descriptions($monster_name) {
	global $monster_zones_info;
	if (!isset($monster_zones_info[$monster_name])) return [];
	$monster_zones = $monster_zones_info[$monster_name];
	$res = [];
	foreach ($monster_zones as $monster_zone) {
		$res_entry = $monster_zone[0];
		$is_special = false;
		$is_ur = false;
		$rejection = 0;
		$copies = 1;
		$special_info = "";
		for ($i = 3; $i < count($monster_zone); $i++) {
			$zone_data = $monster_zone[$i];
			if ($zone_data == 'special') $is_special = true;
			if ($zone_data == 'UR') $is_ur = true;
			if ($zone_data == 'rejection') {
				$i += 1;
				$rejection = (int)$monster_zone[$i];
			}
			if ($zone_data == 'copies') {
				$i += 1;
				$copies = (int)$monster_zone[$i];
			}
			if ($zone_data == 'even' || $zone_data == 'odd') {
				$special_info = $zone_data.' ascensions';
			}
		}
		if ($is_ur) $res_entry = $monster_zone[0]." (ultra-rare)";
		else if ($is_special) $res_entry = $monster_zone[0]." (special)";
		else {
			$res_entry .= " (".$monster_zone[1]."% combat";
			if ((int)$monster_zone[2] > 1) $res_entry .= ", ".$monster_zone[2]." monsters";
			if ($copies > 1) $res_entry .= ", ".$copies." copies";
			if ($rejection > 0) $res_entry .= ", ".$rejection."% rejection rate";
			if ($special_info != "") $res_entry .= ", ".$special_info;
			$res_entry .= ")";
		}
		$res[] = $res_entry;
	}
	return $res;
}